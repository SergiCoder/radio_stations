/// Standardized application size constants
///
/// This class defines the size values used throughout the application,
/// providing a consistent set of sizes for different UI elements.
class AppSizes {
  AppSizes._();

  /// Small icon size (16.0)
  ///
  /// Used for smaller, less prominent icons that appear in text or lists.
  static const double iconSmall = 16;

  /// Medium icon size (24.0)
  ///
  /// Used for standard icons in the UI, such as in buttons or list items.
  static const double iconMedium = 24;

  /// Large icon size (32.0)
  ///
  /// Used for more prominent icons, such as in app bars or featured content.
  static const double iconLarge = 32;

  /// Extra large icon size (48.0)
  ///
  /// Used for very prominent or featured icons, such as primary action buttons.
  static const double iconExtraLarge = 48;

  /// Default icon button size (48.0)
  ///
  /// Default size for icon buttons to ensure adequate touch targets.
  static const double iconButtonSize = 48;

  /// Small avatar size (24.0)
  ///
  /// Used for small avatars or thumbnails.
  static const double avatarSmall = 24;

  /// Medium avatar size (40.0)
  ///
  /// Used for medium-sized avatars or thumbnails.
  static const double avatarMedium = 40;

  /// Large avatar size (56.0)
  ///
  /// Used for large avatars or thumbnails.
  static const double avatarLarge = 56;

  /// Extra large avatar size (64.0)
  ///
  /// Used for extra large avatars or thumbnails.
  static const double avatarExtraLarge = 64;
}
